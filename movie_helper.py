print("Добро пожаловать в кинопомощник!")

mood = input("Как у тебя настроение (весёлое/грустное/нейтральное)? ")
new_movie = input("Хочешь что-то новое? (да/нет) ")

if mood.lower() == "весёлое" and new_movie.lower() == "да":
    print("Совет: посмотри свежую комедию!")
elif mood.lower() == "грустное" and new_movie.lower() == "нет":
    print("Совет: пересмотри классическую драму.")
elif mood.lower() == "нейтральное":
    print("Совет: документалка будет в тему.")
else:
    print("Совет: выбери что угодно, главное – получай удовольствие!")